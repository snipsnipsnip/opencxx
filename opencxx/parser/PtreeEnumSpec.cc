#include <opencxx/parser/PtreeEnumSpec.h>
#include <opencxx/parser/AbstractTranslatingWalker.h>
#include <opencxx/parser/token-names.h>

namespace Opencxx
{

PtreeEnumSpec::PtreeEnumSpec(Ptree* head)
: NonLeaf(head, 0)
{
    encoded_name = 0;
}

int PtreeEnumSpec::What()
{
    return ntEnumSpec;
}

Ptree* PtreeEnumSpec::Translate(AbstractTranslatingWalker* w)
{
    return w->TranslateEnumSpec(this);
}

char* PtreeEnumSpec::GetEncodedName()
{
    return encoded_name;
}

}
